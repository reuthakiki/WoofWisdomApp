package com.example.woofwisdomapplication.DTO;

public class Vaccination {
    private String date;
    private int vaccination_id;
    private String description;
    private String location;
    private String vaccination_name;
    private String username;

    public String getDate() {
        return date;
    }

    public int getVaccination_id() {
        return vaccination_id;
    }

    public String getDescription() {
        return description;
    }

    public String getLocation() {
        return location;
    }

    public String getVaccination_name() {
        return vaccination_name;
    }

    public String getUsername() {
        return username;
    }
}